public class Card{

    public int value;
    public String colour;

    

    public Card(int value, String colour) {
        this.value = value;
        this.colour = colour;
    }



    public int getValue() {
        return value;
    }



    public void setValue(int value) {
        this.value = value;
    }



    public String getColour() {
        return colour;
    }



    public void setColour(String colour) {
        this.colour = colour;
    }

    



}